public class Strings {
    public static void main(String[] args) {

        // Count the number of characters in a string

          String theAlphabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
          System.out.println("The length of the txt string is: " + theAlphabet.length());

        // Convert a string to uppercase

          String lowercaseText = "hello world";
          System.out.println(lowercaseText.toUpperCase());

        // Convert a string to lowercase

          String uppercaseText = "HELLO WORLD";
          System.out.println(uppercaseText.toLowerCase());

        // Find the index of a character in a string

          String myText = "Hello World";
          System.out.println(myText.indexOf("r")); //
        }
      }
